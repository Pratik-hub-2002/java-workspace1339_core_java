package Day8;

public class Datatypes {
	
	
	public static void main(String[] args) {
		
		System.out.println("=============Byte Size=================\n");
		
		System.out.println("Byte size is " + Byte.BYTES + " Byte" );
		System.out.println("Short size is " + Short.BYTES + " Byte");
		System.out.println("Integer size is "+ Integer.BYTES + " Byte");
		System.out.println("Long size is " + Long.BYTES + " Byte");
		System.out.println("Float size is " + Float.BYTES + " Byte");
		System.out.println("Double size is " + Double.BYTES + " Byte");
		System.out.println("Character size is " + Character.BYTES + " Byte");
		System.out.println("Boolean size is not fixed\n");
		
		
		System.out.println("==============Bits Size=================\n");
		
		System.out.println("Byte size is " + Byte.SIZE + " Bits" );
		System.out.println("Short size is " + Short.SIZE + " Bits");
		System.out.println("Integer size is "+ Integer.SIZE + " Bits");
		System.out.println("Long size is " + Long.SIZE + " Bits");
		System.out.println("Float size is " + Float.SIZE + " Bits");
		System.out.println("Double size is " + Double.SIZE + " Bits");
		System.out.println("Character size is " + Character.SIZE + " Bits");
		System.out.println("Boolean size is not fixed");
	}

}
